char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (dest[i])
		i++;
	while (src[k])
	{
		dest[i+k] = src[k];
		k++;
	}
	dest[k] = '\0';
	return (dest);
}
