int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb == 1 || nb == 0)
		return (0);
	if (nb < 0)
		return (0);
	while (i <= nb / 2)
	{
		if (nb % i != 0)
			i++;
		else
			return (0);
	}
	return (1);
}
