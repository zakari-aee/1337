#include <unistd.h>

void	ft_is_negative(int n)
{
	char	ng;
	char	po;

	ng = 'N';
	po = 'P';
	if (n < 0)
	{
		write(1, &ng, 1);
	}
	else
	{
		write(1, &po, 1);
	}
}
